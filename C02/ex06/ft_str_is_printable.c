int	ft_str_is_printable(char *str)
{
	int flag;

	flag = 1;
	while (*str != '\0')
	{
		if (!(*str >= ' ' && *str <= '~'))
		{
			flag = 0;
		}
		str++;
	}
	return (flag);
}
