char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = dest;
	while (*src != '\0' && n > 0)
	{
		*dest = *src;
		src++;
		dest++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (ptr);
}
